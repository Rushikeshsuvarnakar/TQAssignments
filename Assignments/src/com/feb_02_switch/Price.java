package com.feb_02_switch;

public class Price {

	public static void main(String[] args) {
		int price = 4;
		switch (price) {
		case 1:
			System.out.println("It is :2");

		default:
			System.out.println("It is :default");

		case 5:
			System.out.println("It is 5");

		case 9:
			System.out.println("It is 90");
		}

	}

}

